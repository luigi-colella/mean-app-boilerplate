export default interface iUser {
  /**
   * The ID of user.
   */
  id: string

  /**
   * The name of user.
   */
  name: string

  /**
   * The surname of user.
   */
  surname: string

  /**
   * The img URL of user's avatar.
   */
  img: string

  /**
   * The phone number of user.
   */
  number: string

  /**
   * The email of user.
   */
  email: string
}
